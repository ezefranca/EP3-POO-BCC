    private void cboxOpcaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboxOpcaoActionPerformed
        labelOpcao.setText("Escolher um " + cboxOpcao.getSelectedItem().toString());
        if (null != cboxOpcao.getSelectedItem().toString()) {
            switch (cboxOpcao.getSelectedItem().toString()) {
                case "Dentista":
                    local = localDentistas;
                    break;
                case "Medico":
                    local = localMedicos;
                    break;
                case " ":
                    local.add("");
            }
        }

        model = new DefaultListModel<>();
        for (String local1 : local) {
            model.addElement(local1);
        }
        listaMedicos.setModel(model);
        listaMedicos.setSelectedIndex(0);
    }//GEN-LAST:event_cboxOpcaoActionPerformed

    private void btnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarActionPerformed
        Boolean ocupado = false;
        Agenda salvar = new Agenda();
        String data = campoData.getText();
        String medico = listaMedicos.getSelectedValue().toString();
        String hora = cboxHora.getSelectedItem().toString();
        String paciente = localNome;
        ////
        String checkLogin = "";
        String name = "";
        for (ArrayList<String> user : salvar.ler(paciente)) {
            for (int j = 0; j < 2; j++) {
                if (medico.equals(user.get(j))) {
                    if (data.equals(user.get(j + 1))) {
                        if (hora.equals(user.get(j + 2))) {
                            if (!caixaAviso.isVisible()) {
                                ocupado = true;
                                JPanel messagePane = new JPanel();
                                messagePane.add(new JLabel("Data/horário ocupado, escolha outros!"));
                                System.out.println("Data ocupada, escolha outra!");
                                caixaAviso.setBounds(300, 300, 250, 150);
                                caixaAviso.setContentPane(messagePane);
                                caixaAviso.setTitle("AVISO");
                                caixaAviso.setLocationRelativeTo(this);
                                caixaAviso.setVisible(true);
                                break;
                            }
                        }
                    }
                }
            }
        }
        if (!ocupado) {
            salvar.gravar(medico, data, hora, paciente);
            JPanel messagePane = new JPanel();
            messagePane.add(new JLabel("Consulta Marcada com Sucesso!"));
            System.out.println("Consulta Marcada");
            caixaAviso.setBounds(300, 300, 250, 100);
            caixaAviso.setContentPane(messagePane);
            caixaAviso.setTitle("Sucesso");
            caixaAviso.setLocationRelativeTo(this);
            caixaAviso.setVisible(true);
            
        }
        ////

        
        //System.out.println(salvar.toString());
    }//GEN-LAST:event_btnAgendarActionPerformed

